#include "csu_Encoder.h"

//---------------------------------------------------------------------------
// 전역 변수
//---------------------------------------------------------------------------
uint32_t encPosition = 0;

//---------------------------------------------------------------------------
// Encoder_Init
//---------------------------------------------------------------------------
void Encoder_Init(void)
{
    // HAL 초기화 호출 (SPI-C 설정)
    Encoder_Init_Hardware();
}

//---------------------------------------------------------------------------
// Encoder_UpdatePosition (1ms 또는 EPWM 인터럽트에서 주기적 호출)
//---------------------------------------------------------------------------
void Encoder_UpdatePosition(void)
{
    // SPI-C 통신을 통해 엔코더 원시(Raw) 데이터를 수신
    uint32_t rawData = Encoder_ReadSpiData();
    
    // 필요 시 데이터 파싱 (SSI 포맷에 맞춰 에러/워닝 비트 분리 등)
    // 여기서는 수신한 데이터 전체를 위치값으로 저장
    encPosition = rawData;
}

